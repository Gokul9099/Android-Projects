package com.example.hw05_group26;

public class Source {
    String id;
    String name;

    public Source() {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
